package ru.skypro;

public class Task1 {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 10) {
            System.out.print(i + " ");
            i++;
        }
        System.out.println(" ");
        for (int x = 10; x >= 1; x--) {
            System.out.print(x + " ");
        }
    }
}
